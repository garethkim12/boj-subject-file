N = int(input())
F = int(input())

# 1. N의 마지막 두 자리를 00으로 바꿉니다.
# 275 -> 200 (100으로 나눈 몫에 다시 100을 곱함)
temp_N = (N // 100) * 100

# 2. 00부터 99까지 1씩 더하며 F로 나누어지는지 확인
while temp_N % F != 0:
    temp_N += 1

# 3. 정답의 마지막 두 자리만 추출
# 204라면 04가 필요하므로 100으로 나눈 나머지를 구함
ans = temp_N % 100

# 4. 출력 (두 자리 형식을 맞춰야 함)
if ans < 10:
    print(f"0{ans}")
else:
    print(ans)